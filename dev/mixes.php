                <h3>Sample Rates and Bit Depth</h3>

                <h4>Mastering Really Start With Tracking</h4>

                <p>As the digital audio worksation has become a nearly ubiquitous tool in music production, it's important to realize the choices you make when starting the recording process will ripple through to mixing and mastering.</p>

                 <p>Two of the more important decisions you'll make is choosing a sample rate and bit depth. There are a number of options for each and various reasons why you'd want to choose one over the other.</p>

                <p></p>

                <h4>Sample Rate</h4>
                <p>Your choice of sample rate will usually be determined by where your audio is ultimatly going to be used. At a minimum, you'll want to record at 44.1 kHz which is the standard for the Compact Disc (CD), but as the CD becomes obsolete and digital delivery now dominates the world, you should strongly consider a minimum of 48 kHz instead.</p>

                <p>Another reason to choose 48 kHz is because it's the de facto standard for most video projects. If you're thinking of trying to get your music placed in television or movies (and you should!), 48 kHz is the way to go.</p>

                <p>A number of organizations such as <a href="https://www.grammy.com/recording-academy/membership/recording-academy/about/chapters/producers-engineers-wing">The Recording Academy Producers & Engineers Wing</a> and <a href="https://www.riaa.com/" rel="external">The Recording Industry Association of America (RIAA)</a> have also <a href="https://www.riaa.com/high-resolution-audio-initiative-gets-major-boost-with-new-hi-res-music-logo-and-branding-materials-for-digital-retailers/" rel="external">agreed to define high resolution audio</a> as "<em>recordings which have been mastered from better than CD quality (48 kHz/20-bit or higher) music sources</em>."</p>

                <p>You can certainly record at much higher sample rates such as 96 kHz or more. The are a few techinal reasons why this may provide some advantages, but I encourage you to experiment and actually listen to see if you personally hear the difference.</p>

                <p>For the typical musician, a 48 kHz sample rate will sound great. If you are going for more of an audiophile approach or are targeting an esoteric delivery format with a specification that supports higher sample rates, go with what makes sense for you.


                <h4>Bit Depth</h4>

                <p>For many of the same reasons above, choosing a 24 bit file format to send to mastering (vs 16 bit) is going to be your ideal option and will provide an ample dynamic range and signal to noise ratio. If you're also targeting your release for <a href="https://www.apple.com/itunes/mastered-for-itunes/" rel="external">Apple Digital Masters</a>, your mixes must be at least 24 bit.</p>

                <p>Another choice your workstation may allow is a 32 bit floting point file. This bit depth allows for storage of a much wider range of values and also has some advantages </p>

                <p>Pretty much every digital audio workstation does its internal math at 32 bit or more likley 64 bit resolution, and will then reduce that down to whatever bit depth you have choosen for file creation and storage. Plugs-in may not be able to operate with the same precision</p>

                <p>Keep in mind when you are listening to your music, there is a Digital to Analog (DAC) converter at the end of your chain that is more than likely operating at 24 bit resolution. There are many DACs out there that now operate at 32 bit resolution (integer, not floating point!), but if you look at their specfifications for dynamic range, they are still under the 144 dB that 24 bits offer.</p>

                 <p>mastering enigneer with D/A through an analog chain </p>

                <h4>What To Send To Mastering</h4>
                
                <p>The short answer is send 24 bit WAV or AIFF files <strong><em>at the same sample sample rate as your mixes. Do not sample rate convert your files</em></strong>. Upsampling files doesn't make them sound better and downsampling will likley make them sound worse.</p>

                <p>Also note that higher sample rates and bit depths will increase file size, and will tax your audio workstation more if that is a concern. You might also find that some of your plugins may not work at the highest sample rates.</p>

                <p>While hard disk space is realtively inexpensive, a 48 kHz / 24 bit file will typically provide excellent sound quality at a nominal file size, while also positioning you well for a potential marraige with video. 96 kHz / 24 bit is another great choice.</p>

                <p>The goal throughout the music production process is to keep your audio at the highest quality possible. Once mastering is complete, your mastering engineer can create any files you need with great sounding sample rate and bit depth conversions. They can also create high quality encodes to lossy formats such as MP3 or AAC.</p>
