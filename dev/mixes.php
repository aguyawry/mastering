                <h4>What To Send To Mastering</h4>
                <p>The short answer is, send 24 bit WAV or AIFF files <strong><em>at the same sample sample rate as your mixes. Do not sample rate convert your files</em></strong>. Upsampling files doesn't make them sound better and downsampling will likley make them sound worse.</p>

                <p>Also note that higher sample rates and bit depths will increase file sizes, and will tax your audio workstation more if that is a concern. While hard disk space is realtively inexpensive, a 48 kHz / 24 bit file will typically provide excellent sound quality at a nominal file size, while also positioning you well for a potential marraige with video. 96 kHz / 24 bit is another great choice.</p>

                <p>The goal throughout the music production process is to keep your audio at the highest quality possible. Once mastering is complete, your mastering engineer can create high quality sample rate and bit depth conversions as needed. They can also create high quality encodes to formats such as MP3 or AAC.</p>

                    


