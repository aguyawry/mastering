                <h4>Sample Rate</h4>
                <p>Your choice of sample rate will usually be determined by where your audio is ultimatly going to be used. At a minimum, you'll want to record at 44.1 kHz which is the standard for the Compact Disc, but as the CD becomes obsolete and streaming services now dominate the world, you should consider a minimum of 48 kHz instead.</p>

                <p>Another reason to consider 48 kHz is because it's the de facto standard for pretty much all video projects. If you're thinking of trying to get your music placed in television or movies (and you should!), 48 kHz is the way to go.</p>

                <p>A number of organizations such as <a href="https://www.grammy.com/recording-academy/membership/recording-academy/about/chapters/producers-engineers-wing">The Recording Academy Producers & Engineers Wing</a> and <a href="https://www.riaa.com/" rel="external">The Recording Industry Association of America (RIAA)</a> have also actually defined "high resolution" audio as <em>recordings which have been mastered from better than CD quality (48 kHz/20-bit or higher) music sources</em>.</p>

                <p>You can certainly record at much higher sample rates such as 96 kHz or more. I encourage you to experiment by listening to see if you personally hear the difference. In the early 2000's, there was a trend to record music at higher sample rates due to the format wars between <a href="https://en.wikipedia.org/wiki/DVD-Audio" rel="external">DVD-Audio</a> and the <a href="https://en.wikipedia.org/wiki/Super_Audio_CD" rel="external">Super Audio CD (SACD)</a> which was encoded via <a href="https://en.wikipedia.org/wiki/Direct_Stream_Digital" rel="external">Direct Stream Digital (DSD)</a>, not via <a href="https://en.wikipedia.org/wiki/Pulse-code_modulation" rel="external">Pulse-Code Modulation (PCM)</a>.</p>

                <p>Since then, consumers' tastes have trended towards downloadable music vs physical discs. For the typical musician, a 48 kHz sample rate will provide an ample dynamic range and signal to noise ratio. If you are going for more of an audiophile approach, by all means consider a higher sample rate.

